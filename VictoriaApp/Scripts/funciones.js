
$("#cbEnterar").blur(function () {
    if (document.getElementById("cbEnterar").selectedIndex != 0) {
        $(this).css({ "background": "#A8A8F2" })
        $(this).css({ "color": "#ffff" })
    } else {
        $(this).css({ "background": "transparent" })
        $(this).css({ "color": "#495057" })
    }
});
